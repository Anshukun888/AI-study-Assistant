# ==================== Collaborative Study Groups ====================

class StudyGroup(Base):
    """Study group for collaborative learning."""
    __tablename__ = "study_groups"

    id = Column(Integer, primary_key=True, index=True)
    name = Column(String(255), nullable=False)
    created_by = Column(Integer, ForeignKey("users.id", ondelete="CASCADE"), nullable=False)
    created_at = Column(DateTime(timezone=True), server_default=func.now())

    # Relationships
    creator = relationship("User", foreign_keys=[created_by])
    memberships = relationship("GroupMembership", back_populates="group", cascade="all, delete-orphan")
    messages = relationship(
        "GroupMessage", back_populates="group", cascade="all, delete-orphan",
        foreign_keys="GroupMessage.group_id",
    )
    documents = relationship("GroupDocument", back_populates="group", cascade="all, delete-orphan")
    pinned = relationship("PinnedGroupMessage", back_populates="group", uselist=False, cascade="all, delete-orphan")
    invites = relationship("GroupInvite", back_populates="group", cascade="all, delete-orphan")


class GroupMembership(Base):
    """User membership in a study group with role."""
    __tablename__ = "group_memberships"

    id = Column(Integer, primary_key=True, index=True)
    user_id = Column(Integer, ForeignKey("users.id", ondelete="CASCADE"), nullable=False)
    group_id = Column(Integer, ForeignKey("study_groups.id", ondelete="CASCADE"), nullable=False)
    role = Column(String(20), nullable=False, default="member")  # admin | member
    joined_at = Column(DateTime(timezone=True), server_default=func.now())

    # Relationships
    user = relationship("User", back_populates="group_memberships")
    group = relationship("StudyGroup", back_populates="memberships")


class GroupMessage(Base):
    """Message in a group chat. user_id is null for AI messages; sender_type='ai' for AI."""
    __tablename__ = "group_messages"

    id = Column(Integer, primary_key=True, index=True)
    group_id = Column(Integer, ForeignKey("study_groups.id", ondelete="CASCADE"), nullable=False)
    user_id = Column(Integer, ForeignKey("users.id", ondelete="SET NULL"), nullable=True)
    sender_type = Column(String(20), nullable=False, default="user")  # user | ai
    message = Column(Text, nullable=False)
    message_type = Column(String(20), nullable=False, default="text")  # text | file | ai
    file_path = Column(String(500), nullable=True)  # relative path for file messages
    file_name = Column(String(255), nullable=True)
    message_status = Column(String(20), nullable=False, default="sent")  # sent | delivered
    group_file_id = Column(Integer, ForeignKey("group_documents.id", ondelete="SET NULL"), nullable=True)  # optional attached file for AI context
    created_at = Column(DateTime(timezone=True), server_default=func.now())

    # Relationships
    group = relationship("StudyGroup", back_populates="messages", foreign_keys=[group_id])
    user = relationship("User", back_populates="group_messages", foreign_keys=[user_id])
    votes = relationship("MessageVote", back_populates="message", cascade="all, delete-orphan")


class GroupDocument(Base):
    """Document (PDF/image) shared in a group; extracted text for AI context."""
    __tablename__ = "group_documents"

    id = Column(Integer, primary_key=True, index=True)
    group_id = Column(Integer, ForeignKey("study_groups.id", ondelete="CASCADE"), nullable=False)
    file_name = Column(String(255), nullable=False)
    file_path = Column(String(500), nullable=False)  # path under uploads/groups/{group_id}/
    extracted_text = Column(Text, nullable=True)
    uploaded_by = Column(Integer, ForeignKey("users.id", ondelete="SET NULL"), nullable=True)
    created_at = Column(DateTime(timezone=True), server_default=func.now())

    # Relationships
    group = relationship("StudyGroup", back_populates="documents")
    uploader = relationship("User", foreign_keys=[uploaded_by])


class MessageVote(Base):
    """Upvote on a group message (topic voting)."""
    __tablename__ = "message_votes"

    id = Column(Integer, primary_key=True, index=True)
    user_id = Column(Integer, ForeignKey("users.id", ondelete="CASCADE"), nullable=False)
    message_id = Column(Integer, ForeignKey("group_messages.id", ondelete="CASCADE"), nullable=False)
    created_at = Column(DateTime(timezone=True), server_default=func.now())

    # Relationships
    user = relationship("User", back_populates="message_votes")
    message = relationship("GroupMessage", back_populates="votes")


class PinnedGroupMessage(Base):
    """Single pinned message per group (one row per group)."""
    __tablename__ = "pinned_group_messages"

    id = Column(Integer, primary_key=True, index=True)
    group_id = Column(Integer, ForeignKey("study_groups.id", ondelete="CASCADE"), nullable=False, unique=True)
    message_id = Column(Integer, ForeignKey("group_messages.id", ondelete="CASCADE"), nullable=False)
    pinned_by = Column(Integer, ForeignKey("users.id", ondelete="SET NULL"), nullable=True)
    pinned_at = Column(DateTime(timezone=True), server_default=func.now())

    # Relationships
    group = relationship("StudyGroup", back_populates="pinned")
    message = relationship("GroupMessage")
    user = relationship("User", foreign_keys=[pinned_by])


class GroupInvite(Base):
    """Secure invite link for a study group. Token in URL: /groups/invite/{token}."""
    __tablename__ = "group_invites"

    id = Column(Integer, primary_key=True, index=True)
    group_id = Column(Integer, ForeignKey("study_groups.id", ondelete="CASCADE"), nullable=False)
    token = Column(String(64), unique=True, nullable=False, index=True)
    expires_at = Column(DateTime(timezone=True), nullable=False)
    usage_limit = Column(Integer, nullable=True)  # null = unlimited
    used_count = Column(Integer, nullable=False, default=0)
    created_at = Column(DateTime(timezone=True), server_default=func.now())

    # Relationships
    group = relationship("StudyGroup", back_populates="invites")
